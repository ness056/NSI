def exercice5():
    def diviseurs(n: int) -> list:
        div = []
        for m in range(2, math.ceil(n / 2)+1):
            if (n / m).is_integer():
                div.append(m)
        return div

    def est_premier(n: int) -> bool:
        if n != 1 and len(diviseurs(n)) == 0:
            return True
        else:
            return False

    def dico_est_premier(n: int) -> dict:
        d = {}
        for m in range(1, n + 1):
            d[m] = est_premier(m)
        return d

    print(dico_est_premier(10))

